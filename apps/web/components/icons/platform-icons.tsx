interface PlatformIconProps {
  platform: string;
  size?: "sm" | "md" | "lg";
  className?: string;
}

export function PlatformIcon({ platform, size = "md", className = "" }: PlatformIconProps) {
  const sizeMap = { sm: 16, md: 20, lg: 24 };
  const containerSize = { sm: "h-8 w-8", md: "h-10 w-10", lg: "h-12 w-12" };
  const iconSize = sizeMap[size];

  const config = platformIconMap[platform];

  if (!config) {
    // Fallback for unknown platforms
    return (
      <div className={`flex items-center justify-center rounded-lg bg-gray-100 text-xs font-bold text-gray-500 ${containerSize[size]} ${className}`}>
        {platform.slice(0, 2)}
      </div>
    );
  }

  const IconComponent = config.icon;

  return (
    <div className={`flex items-center justify-center rounded-lg ${config.bgColor} ${containerSize[size]} ${className}`}>
      <IconComponent className={config.color} size={iconSize} />
    </div>
  );
}
